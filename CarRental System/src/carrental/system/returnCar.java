    private void car_idKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_car_idKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER)
        {

            try {

                System.out.println("hi");
                String car_id1 =car_id.getText();

               
                con = DriverManager.getConnection("jdbc:mysql://localhost/carrentalsystem","root","zulfa123");
                pst = con.prepareStatement("select car_id,cust_id,fee,due,DATEDIFF(NOW(),due) as elap from rental where car_id = ? ");
                pst.setString(1,car_id1);
                rs = pst.executeQuery();

                if(rs.next()==false)
                {

                    JOptionPane.showMessageDialog(null, "Car No. not Found");
                }
                else
                {
                    String productname = rs.getString("cust_id");
                    txt_custid.setText(productname.trim());

                    String datedue = rs.getString("due");
                    txtdue.setText(datedue.trim());

                    String elp = rs.getString("elap");
                    //  JOptionPane.showMessageDialog(this, elp);

                    int cost = rs.getInt("fee");
                    int elaped = Integer.parseInt(elp);

                    if(elaped>0)
                    {
                        txtelp.setText(elp);

                        int fine = elaped * 100;

                        cost = cost+fine;
                        txtfine.setText(String.valueOf(fine));
                        JOptionPane.showMessageDialog(null, "Total cost = Rs"+ cost);

                    }
                    else
                    {
                        txtelp.setText("0");
                        txtfine.setText("0");
                        JOptionPane.showMessageDialog(null, "Total cost = Rs"+ cost);

                    }
                    

                }

            } catch (SQLException ex) {
                Logger.getLogger(returnCar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_car_idKeyPressed

